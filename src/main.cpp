int16_t linePos = 0;

void loop()
{
    int prevLinePos = linePos;
    // ask user where to draw line
    if (Serial.available() > 0)
    {
        linePos = Serial.parseInt();
        Serial.println(linePos);
    }

    // draw line
    if (linePos != prevLinePos && linePos >= 0 && linePos < PANEL_WIDTH_X)
    {
        Serial.println("Screen Height: " + PANEL_HEIGHT_Y);
        Serial.println("Screen Width: " + PANEL_WIDTH_X);
        dma_display->clearScreen();
        dma_display->drawPixel(0, linePos, 255);
        prevLinePos = linePos;
    }
}